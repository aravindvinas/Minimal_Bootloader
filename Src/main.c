static void flash_bin(uint8_t *buf)
{
  int i = 0;
  uint64_t data = 0;
  while(i<224)
  {
	  data |= ((uint64_t) buf[i] << 56);
	  data |= ((uint64_t) buf[i+1] << 48);
	  data |= ((uint64_t) buf[i+2] << 40);
	  data |= ((uint64_t) buf[i+3] << 32);
	  data |= ((uint64_t) buf[i+4] << 24);
	  data |= ((uint64_t) buf[i+5] << 16);
	  data |= ((uint64_t) buf[i+6] << 8);
	  data |= ((uint64_t) buf[i+7]);

    HAL_FLASH_Program(FLASH_TYPEPROGRAM_WORD, (flash_base + i), data);

    i += 8;
  }
  jump_user();
}

